export interface IntervaloOpcao {
  rotulo: string;
  texto: string;
}

export interface PosologiaOpcao {
  rotulo: string;
  resumo: string;
  textoReceita: string;
  observacao?: string;
  intervalos?: IntervaloOpcao[];
}

export function aplicarIntervalo(
  template: string,
  intervalo: IntervaloOpcao | null,
  modo: "resumo" | "texto",
): string {
  if (!intervalo) return template.replace(/\s*\{intervalo\}/g, "");
  const sub = modo === "texto" ? intervalo.texto : intervalo.rotulo;
  return template.replace(/\{intervalo\}/g, sub);
}

