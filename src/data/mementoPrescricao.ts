export function agruparPorCategoria(
  meds: MedicamentoMemento[],
  filtroCategoria: CategoriaMemento | "todas" = "todas",
) {
  if (filtroCategoria !== "todas") {
    const cat = CATEGORIAS_MEMENTO.find((c) => c.key === filtroCategoria);
    if (!cat) return [];
    const items = meds.filter((m) => m.categorias.includes(filtroCategoria));
    return items.length > 0 ? [{ info: cat, items }] : [];
  }
  const map = new Map<CategoriaMemento, MedicamentoMemento[]>();
  CATEGORIAS_MEMENTO.forEach((c) => map.set(c.key, []));
  for (const m of meds) {
    const primeiraCategoria = CATEGORIAS_MEMENTO.find((c) => m.categorias.includes(c.key));
    if (primeiraCategoria) map.get(primeiraCategoria.key)!.push(m);
  }
  return CATEGORIAS_MEMENTO
    .map((c) => ({ info: c, items: map.get(c.key) ?? [] }))
    .filter((g) => g.items.length > 0);
}

export function contarPorCategoria(cat: CategoriaMemento): number {
  return MEDICAMENTOS_MEMENTO.filter((m) => m.categorias.includes(cat)).length;
}

